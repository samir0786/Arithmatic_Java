package com.bl.demo;

public class Arithmetic {
    public int subtract(int a, int b){
        return a-b;
    }

    public int add(int a, int b) {
        return a+b;
    }

    public int division(int a, int b) {
        return a/b;
    }

    public int Multiplication(int a, int b) {
        return a*b;

    }
}
